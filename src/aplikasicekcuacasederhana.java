    public static class WeatherService {

        private static final String API_KEY = "abcee887615b56d1247fbc18926f091a";
        private static final String BASE_URL = "http://api.openweathermap.org/data/2.5/weather?q=";

        public static String getWeatherData(String city) {
            StringBuilder result = new StringBuilder();

            try {
                String urlString = BASE_URL + city + "&units=metric&appid=" + API_KEY;
                URL url = new URL(urlString);

                HttpURLConnection conn = (HttpURLConnection) url.openConnection();
                conn.setRequestMethod("GET");

                BufferedReader rd = new BufferedReader(new InputStreamReader(conn.getInputStream()));
                String line;
                while ((line = rd.readLine()) != null) {
                    result.append(line);
                }
                rd.close();
            } catch (Exception e) {
                e.printStackTrace();
                return null;
            }
            return result.toString();
        }
    }

    private Map<String, String> parseWeatherData(String dataCuaca) {
        Map<String, String> weatherInfo = new HashMap<>();
        try {
            JSONObject jsonObject = new JSONObject(dataCuaca);
            JSONObject main = jsonObject.getJSONObject("main");
            JSONObject weather = jsonObject.getJSONArray("weather").getJSONObject(0);

            double temperature = main.getDouble("temp");
            weatherInfo.put("temperature", String.format("%.1f", temperature));

            String description = weather.getString("description");
            weatherInfo.put("description", description);

            String weatherType = determineWeatherType(temperature);
            weatherInfo.put("weatherType", weatherType);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return weatherInfo;
    }

    private String getWeatherIconPath(String weatherType) {
        if (weatherType == null) {
            weatherType = "unknown"; // Default to "unknown" if weatherType is null
        }

        // Choose the appropriate icon based on the weather type
        switch (weatherType) {
            case "hot":
                return "sun.png";
            case "moderate":
                return "cloud.png";
            case "cold":
                return "snow.png";
            default:
                return "unknown.png"; // Default icon if no match
        }
    }

    private String determineWeatherType(double temperature) {
        if (temperature > 30) {
            return "hot";
        } else if (temperature >= 15 && temperature <= 30) {
            return "moderate";
        } else {
            return "cold";
        }
    }
    
    
